          <div class="project-description-container">
            <div class="project-description-wrapper">
              <h5 class="project-description-header">Visual Exploration</h5>
              <p> There are more than 500 varieties of avocados. 
                  However, research revealed the Hass breed accounts for 95% of avocados consumed in the United States <sup>[<a href="https://www.washingtonpost.com/news/wonk/wp/2015/01/22/the-sudden-rise-of-the-avocado-americas-new-favorite-fruit/" style="color:#b985c3;" target="_blank">1</a>]</sup>. 
                  I used the Hass throughout the project because I belived it would have the most prototypical form for a U.S. target audience.
              </p>
              <p> Research reveleaed that Avocado is usually eaten raw, as heat can ruin the flavor.
                  It is often served by itself, on toast, mashed into guacomle, or inside sandwiches or tacos.
                  I began visual exploration by preparing some of these foods for myself.
                  Throughout the process I paused to sketch and photograph the avocado.
                  The following characteristics were noteworthy:
              </p>
              <ul>
                <li>perfect geometric forms created by the knife</li>
                <li>subtle yellow-green gradient</li>
                <li>smooth taste and high fat content</li>
                <li>twisting motion used to separate the halves</li>
              </ul>
            </div>
          </div>


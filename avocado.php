              <h5 class="project-description-header">Background</h5>
              <p> I was tasked with designing a logo for a fictional nonprofit marketing board â€“ The National Avocado Association. 
                  This entity's mission would be to promote the consumption of avocados in the United States. 
                  Project requirments included the logo function,
                  <ul>
                    <li>using a single color (e.g. black ink on white paper) </li>
                    <li>as small as 16x16 pixels (a website <a href="https://en.wikipedia.org/wiki/Favicon" style="color:#b985c3;" target="_blank" rel="noopener">favicon</a>)</li>
                  </ul>
              </p>
            </div>

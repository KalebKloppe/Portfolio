          <div class="project-description-container">
            <div class="project-description-wrapper">
              <p> The audience at WaterFire was disproportionately made up of families.
                  After mind-mapping, the idea of community quickly connected with themes like Christmas and family.
                  The benefit of community seemed to fit the audience’s motives better than the benefit of novelty.
                  The second question could be answered: The benefit of WaterFire is to bring people together.
              </p>
              <p> Now, the third and fourth questions needed answers: 
                  What is the purpose of this booklet? 
                  And how will this booklet accomplish its purpose?
              </p>
              <p> However, there was a problem.
                  The provided copy was not well suited to a promotional purpose.
                  It was lengthy, indirect, and written in a passive voice, describing the event instead of advocating that the reader visit.
              </p>
              
              <p class="block-quote"> WaterFire® is a work of art that involves movement, participation and surprise. When visitors encounter WaterFire, they cannot absorb the sculpture from just their sense of sight or even from a single vantage point—they must walk through the installation and they must use all five of their senses.</p>
              <p class="caption"> excerpt of provided copy </p>
              <P> To convert this to promotional copy "They must walk through the installation and they must use all five of their senses" could easily be rewritten to "Walk through WaterFire. Use all of your senses."
                  However, rewriting or cutting of the copy was not permitted.
              </p> 
              <p> To solve this problem, the idea of a promotional brochure had to be discarded.
                  Instead, the purpose of the booklet would be to enhance the WaterFire experience, a booklet that brings people together in a shared experience.
                  This would be accomplished by designing the booklet to be read by two people at once.
              </p>
            </div>
          </div>

          <div class="project-description-container">
            <div class="project-description-wrapper">
              <h5 class="project-description-header">Design</h5>
              <p> The first consideration when designing a two-person booklet was format.
                  Using a partner, different reading arrangements were experimented with.
                  Having partners standing opposed (example 3) achieved a good balance of intimacy and worked best for readers of different height.
              </p>
            </div>
          </div>


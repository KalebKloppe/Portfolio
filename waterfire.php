          <div class="project-description-container">
            <div class="project-description-wrapper">
              <p> The atmosphere at WaterFire was festive.
                  Speakers placed throughout the entire event played a soundtrack designed to enhance the experience.
                  Food booths and other local business had set up tents.
                  These tents were quickly overrun with patrons. </p>
              <p> The atmosphere changed when the bonfires were lit at sunset.
                  Just before the lighting the audience stopped circulating, they became quieter, and the event's music changed pace.
                  After the lighting, the audience stayed quieter than before and circulated more slowly.
                  The crowd began to trickle away, and two hours after the lighting only small groups and couples were left wandering the area. 
                  Around midnight, the bonfires were extinguished and Waterfire concluded.
              </p>
              <p> Having experienced WaterFire for myself, I wrote down a list of potential benefits. 
                  Two potential directions emerged:</p>
              <ul>
                <li><b>Novelty</b>, a reason people travel to new places or start a new hobby. WaterFire transforms the normally quiet Providence riverwalk into a new place worth visiting. </li>
                <li><b>Community</b>, a reason people form new relationships and associate with others. WaterFire brings the people of Providence together in fellowship. </li>
              </ul>
            </div>
          </div>


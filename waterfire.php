          <div class="project-description-container">
            <div class="project-description-wrapper">
              <h5 class="project-description-header">Background</h5>
              <p> I was challenged to create a multi-page print piece for <a href="https://waterfire.org/" style="color:#d6957b;" target="_blank" rel="noopener">WaterFire</a>, an event in downtown Providence, Rhode Island. 
                  At WaterFire, the participation of the community transforms a normally silent riverwalk into a buzzing festival with food, music, and crowds. 
                  The singular project requirement was the use of provided copy in a booklet format. 
                  All other decisions were left up to me.
              </p>
              <p>To start, I created a short list of questions to be answered during the process.</p>
              <ul>
                <li>Who is this booklet for?</li>
                <li>What benefit does the audience get from WaterFire?</li>
                <li>What is the purpose of this booklet?</li>
                <li>How will this booklet accomplish its purpose?</li>
              </ul>
            </div>
          </div>

          <div class="project-description-container">
            <div class="project-description-wrapper">
              <h5 class="project-description-header">Research</h5>
              <p> The project began with field research.
                  I already lived in downtown Providence, so I was able to attend a WaterFire event, take part, and document the experience.
              </p>
              <p> During WaterFire there was a change in the number and type of people downtown.
                  Normally, downtown Providence is limited to a moderate population of college students, businesspersons, and young professionals. 
                  However, during WaterFire I found streets crammed with strollers, children, teenagers, parents, and grandparents.
                  This discovery answered who the booklet was for: families.
              </p>
            </div>
          </div>


              <p> My second consideration was type.
                  Type needed to be large, not only because the viewing distance would increase when two people held the booklet, but because the ambient light level would range from dusk to many small bonfires. 
                  The final typeface is Grad from Mark Simonson Studio, a modernization of Century Schoolbook. 
                  I chose this transitional serif in a bold weight to maximize the legibility of body copy. 
                  The primary size is 15.5pt bold, reached by using a left and right margin of 3em and a target line length of 35 characters (the minimum line length for comfortable reading).
              </p>
